import { RequestHandler, Router } from "express";
import paymentsMidlleware from "../middleware/paymentMiddleware";
import { cancelTransaction, checkTransaction, getStatement, performTransaction } from "../controller/paymentController";


export const paymentRouter: Router = Router();
/**
 * @swagger
 * /transaction:
 *   post:
 *     summary: Perform a transaction for a user
 *     description: Creates a payment record for the specified user and returns updated user data.
 *     tags:
 *       - Transactions
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - userId
 *               - amount
 *             properties:
 *               userId:
 *                 type: integer
 *                 description: The ID of the user making the payment
 *               amount:
 *                 type: number
 *                 description: The amount being paid
 *     responses:
 *       200:
 *         description: Payment successfully created
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                 payment:
 *                   type: object
 *                 user:
 *                   type: object
 *       404:
 *         description: User not found
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *       500:
 *         description: Server error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 */
paymentRouter.post("/createPayment", paymentsMidlleware as RequestHandler, performTransaction as RequestHandler)

/**
 * @swagger
 * /transaction/{id}:
 *   get:
 *     summary: Check transaction details
 *     description: Retrieves transaction details by transaction ID.
 *     tags:
 *       - Transactions
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: integer
 *         description: The ID of the transaction to check
 *     responses:
 *       200:
 *         description: Transaction details retrieved successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                 transaction:
 *                   type: object
 *       404:
 *         description: Transaction or ID not found
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *       500:
 *         description: Server error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 */
paymentRouter.get("/checkTransaction/:id", checkTransaction as RequestHandler)

/**
 * @swagger
 * /transaction/statement:
 *   get:
 *     summary: Get transaction statements within a date range
 *     description: Retrieves all transactions between the specified start and end dates.
 *     tags:
 *       - Transactions
 *     parameters:
 *       - in: query
 *         name: startDate
 *         required: true
 *         schema:
 *           type: string
 *           format: date
 *         description: The start date for filtering transactions (YYYY-MM-DD)
 *       - in: query
 *         name: endDate
 *         required: true
 *         schema:
 *           type: string
 *           format: date
 *         description: The end date for filtering transactions (YYYY-MM-DD)
 *     responses:
 *       200:
 *         description: Transactions retrieved successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                 transactions:
 *                   type: array
 *                   items:
 *                     type: object
 *       404:
 *         description: Missing startDate or endDate
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *       500:
 *         description: Server error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 */
paymentRouter.get("/getStatement", getStatement as RequestHandler)

/**
 * @swagger
 * /transaction/{id}:
 *   delete:
 *     summary: Cancel a transaction
 *     description: Deletes a transaction by its ID.
 *     tags:
 *       - Transactions
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: integer
 *         description: The ID of the transaction to be deleted
 *     responses:
 *       200:
 *         description: Transaction successfully deleted
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *       404:
 *         description: Transaction ID not found
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *       500:
 *         description: Server error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 */
paymentRouter.delete("/deleteTransaction/:id", cancelTransaction as RequestHandler)


